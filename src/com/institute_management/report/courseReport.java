    private void cmbSelectCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbSelectCourseActionPerformed
        courseBean a = new courseBean();
        try {
            String course_id = cmbSelectCourse.getSelectedItem().toString();
            a = courseCon.getCourseDetails(course_id);
            lblDescription.setText(a.getCourseDescription());
            lblClassType.setText(a.getCourseType());
            lblGrade.setText(a.getGrade());
            lblLectureName.setText(a.getLecturerName());
            lblSubject.setText(a.getSubject());
            lblMedium.setText(a.getCourseMedium());
            lblCourseFee.setText(Double.toString(a.getMonthlyFee()));
            
            int array[] = courseCon.totalFreeCards(a.getCourseID());
            lblTotal.setText(Integer.toString(array[3]));
            lblFree.setText(Integer.toString(array[0]));
            lblHalf.setText(Integer.toString(array[1]));
            
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_cmbSelectCourseActionPerformed

