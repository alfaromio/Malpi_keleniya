    private void cmbUserIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbUserIDActionPerformed
        try {
            String userName = cmbUserID.getSelectedItem().toString();
            userBean a = conn.getUserDetails(userName);
            txtAdrres.setText(a.getAddress());
            txtContact.setText(a.getContact());
            txtName.setText(a.getName());
            txtNic.setText(a.getNic());
            txtPassword.setText(a.getPassword());
            txtConfirmPassword.setText(a.getPassword());
            txtUserName.setText(a.getUserName());
            cmUserRole.setSelectedIndex(a.getRoleID() - 1);
            System.out.println(a.getRoleID());
            if (a.getGender().equals("Male")) {
                rbMale.setSelected(true);
                rbFemale.setSelected(false);
            } else {
                rbFemale.setSelected(true);
                rbMale.setSelected(false);
            }
            OldUsername = a.getUserName();

        } catch (Exception ex) {

        }
    }//GEN-LAST:event_cmbUserIDActionPerformed

