    private void loadtblPaymentDetails() {
        try {
            HashMap<Integer, Object[]> tblData = new LectureDbConnection().getPaymentDetailsOnLecturer(staticLB.getLecID());
            DefaultTableModel model = (DefaultTableModel) tblLecPayments.getModel();
            model.setRowCount(0);
            for (int i = 1; i <= tblData.size(); i++) {
                model.addRow(tblData.get(i));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(new JFrame(), "Error in loading Course Name" + e);
        }
    }

