    private void btnPaymentAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPaymentAddActionPerformed
        try { // TODO add your handling code here:
            SimpleDateFormat sdf = new SimpleDateFormat("YYYY-MM-dd");
            String payDate = sdf.format(new Date());
            double amount = Double.parseDouble(txtTotalPayments.getText());
            boolean advance = false;
            String c_id = cmbCourseID.getSelectedItem().toString();
            if (cboxAdvencePay.isSelected()) {
                if (Double.parseDouble(txtAvailableAmount.getText()) > 0) {
                    conn.settleAdvancedPayment(staticLB.getLecID(), cmbMonth.getSelectedItem().toString().toLowerCase(), Integer.parseInt(cmbPaymentYear.getSelectedItem().toString()));
                    JOptionPane.showMessageDialog(new JFrame(), "Advanced Payment Successfully Settled");
                    //update lecture payment table
                    int count = new LectureDbConnection().insertLecPayment(staticLB.getLecID(), payDate, Double.parseDouble(txtAvailableAmount.getText()), cmbMonth.getSelectedItem().toString().toLowerCase(), Integer.parseInt(cmbPaymentYear.getSelectedItem().toString()), c_id);
                    // update the payment table status as 1(paid to the lecturer)
                    conn.updatePaymentsAsPaid(c_id, cmbMonth.getSelectedItem().toString().toLowerCase(), Integer.parseInt(cmbPaymentYear.getSelectedItem().toString()));

                    if (count > 0) {
                        JOptionPane.showMessageDialog(new JFrame(), "Successfully Issue the payment and settle the advanced payment");
                    }

                } else if (Double.parseDouble(txtAvailableAmount.getText()) == 0) {
                    conn.settleAdvancedPayment(staticLB.getLecID(), cmbMonth.getSelectedItem().toString().toLowerCase(), Integer.parseInt(cmbPaymentYear.getSelectedItem().toString()));
                    // update the payment table status as 1(paid to the lecturer)
                    conn.updatePaymentsAsPaid(cmbCourseID.getSelectedItem().toString(), cmbMonth.getSelectedItem().toString().toLowerCase(), Integer.parseInt(cmbPaymentYear.getSelectedItem().toString()));
                    JOptionPane.showMessageDialog(new JFrame(), "Advanced Payment Successfully Settled and No available Amount to issue");

                } else {
                    JOptionPane.showMessageDialog(new JFrame(), "No sufficient amount to settle the advanced payment");
                }

            } else {
                int count = new LectureDbConnection().insertLecPayment(staticLB.getLecID(), payDate, Double.parseDouble(txtAvailableAmount.getText()), cmbMonth.getSelectedItem().toString().toLowerCase(), Integer.parseInt(cmbPaymentYear.getSelectedItem().toString()), c_id);
                // update the payment table status as 1(paid to the lecturer)
                conn.updatePaymentsAsPaid(cmbCourseID.getSelectedItem().toString(), cmbMonth.getSelectedItem().toString().toLowerCase(), Integer.parseInt(cmbPaymentYear.getSelectedItem().toString()));
                if (count > 0) {
                    JOptionPane.showMessageDialog(new JFrame(), "Successfully Issue the payment");
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(new JFrame(), "Error Occured.Data Not Saved");
        }
    }//GEN-LAST:event_btnPaymentAddActionPerformed

