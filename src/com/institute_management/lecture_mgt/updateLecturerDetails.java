    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Double TotalPayments = conn.caculateLecturerPayments(cmbCourseID.getSelectedItem().toString(), cmbMonth.getSelectedItem().toString(), cmbPaymentYear.getSelectedItem().toString(), Double.parseDouble(lblprecentage.getText()));
            txtTotalPayments.setText(TotalPayments + "");
            Double lecturerPayment = (TotalPayments * Double.parseDouble(lblprecentage.getText()) / 100);
            txtLpamount.setText(lecturerPayment + "");
            double advancedPayments = conn.calculateAdvancePayments(Integer.parseInt(staticLB.getLecID()));
            txtUnsettleAdPayments.setText(advancedPayments + "");
            double available_payment_amount = lecturerPayment - advancedPayments;
            txtAvailableAmount.setText(available_payment_amount + "");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(new JFrame(), "Unable to calculate");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

